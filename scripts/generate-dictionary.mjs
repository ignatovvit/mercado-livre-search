const entries = [];
const seen = new Set();

function add(source, sourceLang, targets, category, tags = []) {
  const cleanSource = source.trim();
  const key = `${sourceLang}:${cleanSource.toLowerCase()}`;
  if (!cleanSource || seen.has(key)) return;
  seen.add(key);
  entries.push({
    source: cleanSource,
    sourceLang,
    targets: [targets],
    category,
    tags
  });
}

for (const [ru, en, pt, category, tags] of manualEntries) {
  add(ru, 'ru', pt, category, tags);
  add(en, 'en', pt, category, tags);
}

for (const [ru, en, pt, category] of [...products, ...extraProducts]) {
  add(ru, 'ru', pt, category, [`category:${category}`]);
  add(en, 'en', pt, category, [`category:${category}`]);
  for (const [ruModifier, enModifier, ptModifier] of modifiers) {
    add(`${ruModifier} ${ru}`, 'ru', `${ptModifier} ${pt}`, category, [`category:${category}`]);
    add(`${enModifier} ${en}`, 'en', `${ptModifier} ${pt}`, category, [`category:${category}`]);
    add(`${ru} ${ruModifier}`, 'ru', `${pt} ${ptModifier}`, category, [`category:${category}`]);
    add(`${en} ${enModifier}`, 'en', `${pt} ${ptModifier}`, category, [`category:${category}`]);
  }
}

